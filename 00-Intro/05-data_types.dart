void main(){
  // Number
  int a = 5;
  double pi = 3.1426;
  print("a is : ${a} & pi is : ${pi}");

  // Strings
  var msg = "Hello World";
  print(msg);

  // Boolean
  bool isValid = true;
  print(isValid);

  // Lists
  var list = [1,2,3];
  print(list);

  // Maps
  var student = {'name':'rayhan', 'age':25, 'department':'CSE'};
  print(student);

  // Runes
  var heart_symbol = '\u2665';
  var laugh_symbol = '\u{1f600}';
  print(heart_symbol);
  print(laugh_symbol);
}